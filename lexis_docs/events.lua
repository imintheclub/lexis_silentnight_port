-- ============================================================
-- # Events
-- ============================================================
--
-- ## Functions
--
-- ### subscribe
-- Register a callback to be fired when the event is handled, returning a callback id.
-- ---@param type events.event
-- ---@param callback function(data)
-- function events.subscribe(type, callback): integer
--
-- ### unsubscribe
-- Delete a callback using the id returned from subscribe.
-- ---@param id integer
-- function events.subscribe(id): nil
--
-- ### get
-- Returns an event for a given callback id.
-- ---@param id integer
-- function events.get(id) -> event
--
-- ## Types
--
-- ### event
-- | Type     | Name    |
-- |----------|---------|
-- | bool     | enabled |
-- | function | fn      |
--
-- ### player_join
-- | Type    | Name   |
-- |---------|--------|
-- | player  | player |
-- | integer | slot   |
--
-- ### player_leave
-- | Type    | Name   |
-- |---------|--------|
-- | player  | player |
-- | integer | slot   |
--
-- ### player_flagged
-- | Type    | Name   |
-- |---------|--------|
-- | player  | player |
-- | string  | reason |
--
-- ### log
-- | Type   | Name   |
-- |--------|--------|
-- | string | text   |
-- | color  | color  |
-- | string | prefix |
-- | string | time   |
--
-- ### scroll
-- | Type   | Name   |
-- |--------|--------|
-- | number | offset |
--
-- ### key_press
-- | Type    | Name |
-- |---------|------|
-- | integer | key  |
--
-- ### char_press
